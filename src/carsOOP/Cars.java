package carsOOP; 


public class Cars {
	
	int ruedas; 	// props que tiene un car
	int largo;
	int ancho;
	int motor;
	int peso;
	
	
	//Constructor - siempre tiene el mismo nombre que la clase
	
	public Cars(int ruedas, int largo, int ancho, int  motor, int peso) {
		this.ruedas=ruedas;
		this.largo= largo;
		this.ancho=ancho;
		this.motor=motor;
		this.peso=peso;
		
	}
	

}
